/********** Script for the Video showcase Modal functionality */
const videoShowcaseModal = document.getElementById("video-showcase-modal")
const videoShowcaseMain = document.getElementById("video-showcase-main")
const videoShowcaseIframe = document.getElementById("video-showcase-iframe")
const videoShowcaseTitle = document.getElementById("video-showcase-title")
const videoShowcaseDate = document.getElementById("video-showcase-date")
const videoShowcaseDetails = document.getElementById("video-showcase-details")
const closeVideoShowcaseIcon = document.getElementById("close-video-showcase-icon")
var videoContents = document.querySelectorAll('.video-content');
var videoTitles = document.querySelectorAll('.video-title');
var videoDetails = document.querySelectorAll('.video-details');

//show video showcase modal
function showVideoShowcaseModal() {
    videoShowcaseModal.classList.remove("hidden")
    videoShowcaseModal.classList.add("flex")
}

//close video showcase modal
function closeVideoShowcaseModal() {
    videoShowcaseModal.classList.remove("flex")
    videoShowcaseModal.classList.add("hidden")
}

//close video showcase icon functionality
closeVideoShowcaseIcon.addEventListener("click" , ()=> {
    closeVideoShowcaseModal()
})

////this closes the modal if anywhere outside the main part of the modal has been clicked.
videoShowcaseModal.addEventListener("click" , (event)=> {
    var isClickedInside = videoShowcaseMain.contains(event.target);

    // If the clicked element is outside the div, log a message
    if (!isClickedInside) {
      closeVideoShowcaseModal()
    }
})

// Add click event listener to each "video-content" element
for (let i = 0; i < videoContents.length; i++) {
    videoContents[i].addEventListener("click" , ()=> {
        //show video showcase
        showVideoShowcaseModal();

        //change video showcase title to clicked video
        videoShowcaseTitle.innerHTML = videoTitles[i].textContent

        //change video showcase details to clicked video
        videoShowcaseDetails.innerHTML = videoDetails[i].textContent

        //change video showcase iframe source to clicked video
        videoShowcaseIframe.src = videoContents[i].getAttribute("iframe-src")

        //change video showcase date to clicked video
        videoShowcaseDate.innerHTML = videoContents[i].getAttribute("video-date")
    })
}



/********** Script for the Video showcase Modal functionality */